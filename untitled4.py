    import joblib
    from sklearn.metrics import classification_report, confusion_matrix, roc_auc_score, roc_curve
    import seaborn as sns
    import matplotlib.pyplot as plt
    import pandas as pd

    # Load model dan data uji
    model = joblib.load("best_heart_attack_model.joblib")
    X_test = pd.read_csv("X_test.csv")
    y_test = pd.read_csv("y_test.csv")

    # Prediksi
    y_pred = model.predict(X_test)
    y_proba = model.predict_proba(X_test)[:, 1]

    # METRIK
    st.subheader("ğŸ“Š Evaluasi Model")
    accuracy = (y_pred == y_test.values.ravel()).mean()
    auc = roc_auc_score(y_test, y_proba)

    st.metric("Akurasi", f"{accuracy:.2%}")
    st.metric("AUC Score", f"{auc:.3f}")

    # Classification report
    report = classification_report(y_test, y_pred, output_dict=True)
    report_df = pd.DataFrame(report).transpose()
    st.subheader("ğŸ“‹ Classification Report")
    st.dataframe(report_df.style.format("{:.2f}"))

    # CONFUSION MATRIX
    st.subheader("ğŸ“Œ Confusion Matrix")
    cm = confusion_matrix(y_test, y_pred)
    fig, ax = plt.subplots()
    sns.heatmap(cm, annot=True, fmt="d", cmap="Blues", xticklabels=[0, 1], yticklabels=[0, 1])
    plt.xlabel("Predicted")
    plt.ylabel("Actual")
    st.pyplot(fig)

    # ROC CURVE
    st.subheader("ğŸ“‰ ROC Curve")
    fpr, tpr, _ = roc_curve(y_test, y_proba)
    fig2, ax2 = plt.subplots()
    ax2.plot(fpr, tpr, label=f"AUC = {auc:.2f}")
    ax2.plot([0, 1], [0, 1], "k--")
    ax2.set_xlabel("False Positive Rate")
    ax2.set_ylabel("True Positive Rate")
    ax2.set_title("ROC Curve")
    ax2.legend()
    st.pyplot(fig2)

    # FEATURE IMPORTANCE (Jika Ada)
    st.subheader("ğŸ“Œ Feature Importance")
    try:
        importances = model.named_steps["classifier"].feature_importances_
        features = model.named_steps["preprocessor"].get_feature_names_out()
        imp_df = pd.DataFrame({
            "Feature": features,
            "Importance": importances
        }).sort_values("Importance", ascending=False)

        fig3, ax3 = plt.subplots()
        sns.barplot(x="Importance", y="Feature", data=imp_df.head(10), ax=ax3)
        st.pyplot(fig3)
    except:
        st.info("Feature importance tidak tersedia atau model tidak mendukung.")


